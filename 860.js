/**
 * @param {number[]} bills only 5, 10, 20
 * @return {boolean}
 */
var lemonadeChange = function(bills) {
  const price = 5;
  let changes = {
    5: 0,
    10: 0,
    20: 0,
  };

  while (bills.length) {
    const bill = bills.shift();

    if (bill / price === 1) {
      changes["5"] += 1;
    } else if (bill / price === 2) {
      if (changes["5"] < 1) return false;
      changes["10"] += 1;
      changes["5"] -= 1;
    } else if (bill / price === 4) {
      if (changes["5"] < 1) return false;
      if (changes["10"] < 1 && changes["5"] < 3) return false;
      if (changes["10"] >= 1 && changes["5"] < 1) return false;

      changes["20"] += 1;
      if (changes["10"] >= 1) {
        changes["5"] -= 1;
        changes["10"] -= 1;
      } else {
        changes["5"] -= 3;
      }
    }
  }

  return true;
};

